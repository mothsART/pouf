    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${i}" in
            pouf)
                cmd="pouf"
                ;;
            
            administrative.healthinsurrancecode)
                cmd+="__administrative.healthinsurrancecode"
                ;;
            auto.licenseplate)
                cmd+="__auto.licenseplate"
                ;;
            filesystem.mimetype)
                cmd+="__filesystem.mimetype"
